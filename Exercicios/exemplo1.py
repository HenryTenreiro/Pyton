# ENTRADA DE DADOS (input)
nome = input("Digite o seu nome: ")
idade = int(input("Digite a sua ideade: "))
altura = float(input("Digite a sua altura: "))

# SAIDA DE DADOS (print)
print(f"o seu nome é {nome} e sua idade é de {idade} anos. sua altura é de {altura}")       # f-string
print("o seu nome é", nome, "e sua idade é de", idade, "anos.")                             # usado ,
print("o seu nome é `{} e sua idade é de {} anos.".format(nome, idade))               # .format (variavel)

